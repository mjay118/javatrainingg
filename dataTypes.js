//Primitive Data Type
var a=5;
console.log(typeof a);

var b=5.00;
console.log(typeof b);

var c;
console.log(typeof c);

var d=null;
console.log(typeof d);

var isDisabled=true;
console.log(typeof isDisabled);

var e="Mrityunjay";
console.log(typeof e);

var f='M';
console.log(typeof f);


//Non-Primitive Data Type
//Array
var arr=['a','b','c'];
console.log(typeof arr);
console.log(arr);

//Object
let emp1={
    name: "Ram",
    age: 22,
    salary: 450000,
    dept: "IT Operation"
};
console.log(emp1);